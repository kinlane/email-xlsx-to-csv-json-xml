  <h1>Email Conversion - .xlsx to CSV,JSON,XML</h1>
  <p>This is an evolving weekend project. Working in the federal government, I found myself inundated with Microsoft Excel spreadsheets each day. I wanted a quick and dirty way to convert these to other machine readable formats like CSV, JSON and XML.</p>
  <p>This is a project looking to address this, I just want to be able to email a spreadsheet to a specific address and get back the same data in all three formats. While this isn't a perfect solution, I hope to stabilize it and offer it as an open source project that can be installed anywhere.</p>
  <p>Next i will also provide a screen for uploading files on a web interface in addition to sending via email.</p>
  <p>You can <a href="https://github.com/kinlane/email-xlsx-to-csv-json-xml">follow this project on Github</a>, where you can submit issues. If you would like to be able to use this service as it is, please email me at kinlane@gmail.com or ping me on Twitter @kinlane.</p>
  

